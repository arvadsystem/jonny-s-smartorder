export const isTabAllowed = (permissionSet, tabKey, map, options = {}) => {
  const required = map?.[tabKey] || [];
  return hasAnyPermission(permissionSet, required, options);
};

export const resolveFirstAllowedTab = (tabKeys, permissionSet, map, fallbackKey, options = {}) => {
  const keys = Array.isArray(tabKeys) ? tabKeys : [];
  const firstAllowed = keys.find((key) => isTabAllowed(permissionSet, key, map, options));
  return firstAllowed || fallbackKey || keys[0] || null;
};

export const getModulePermissions = (moduleKey) => MODULE_ROUTE_PERMISSIONS[moduleKey] || [];

export const canAccessModule = (permissionSet, moduleKey, options = {}) =>
  hasAnyPermission(permissionSet, getModulePermissions(moduleKey), options);

export const getVisibleModuleItems = (permissionSet, options = {}) =>
  MODULE_NAV_ITEMS.filter((item) => canAccessModule(permissionSet, item.key, options));

export const getAllowedTabs = (moduleKey, permissionSet, options = {}) =>
  (MODULE_TAB_CONFIG[moduleKey] || []).filter((tab) =>
    hasAnyPermission(permissionSet, tab.required || [], options)
  );

export const getFirstAccessibleDashboardPath = (permissionSet, options = {}) => {
  const nextModule = MODULE_NAV_ITEMS.find(
    (item) => item.key !== 'dashboard' && canAccessModule(permissionSet, item.key, options)
  );

  if (nextModule) return nextModule.path;

  if (canAccessModule(permissionSet, 'perfil', options)) {
    return '/dashboard/perfil';
  }

  return null;
};
